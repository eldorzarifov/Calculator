function addNum(a, b){
    return a + b;
}

function subtractNum(a, b){
    return a - b;
}

function multiplyNum(a, b){
    return a * b;
}

function divideNum(a, b){
    return a / b;
}

function percentage(a, b){
    return (a/100)*b;
}
function squareRootNum(a, b){
    return a ** (1/b);
}

function powerNum(a, b){
    return a ** b;
}

function plusminusNum(a){
    return -a;
}

